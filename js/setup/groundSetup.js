const createGround = (scene) => {
    const ground = BABYLON.MeshBuilder.CreateGround("ground", {
        width: GROUND_WIDTH,
        height: GROUND_HEIGHT,
        subdivsions: 4
    }, scene);

    var groundColor = new BABYLON.StandardMaterial(scene);
    groundColor.alpha = 1;
    groundColor.diffuseColor = new BABYLON.Color3(0.3, 0.2, 0.7);

    var mat = new BABYLON.StandardMaterial("textVid", scene);
    mat.diffuseTexture = new BABYLON.Texture("../ground-texture/ground1.jpg", scene);
    mat.diffuseTexture.vScale = 5.0;
    mat.diffuseTexture.uScale = -5.0;
    mat.backFaceCulling = false;

    ground.material = mat;

    ground.position.x = INIT_POS_X;
    ground.position.z = INIT_POS_Z;
}